(** * Kleisli categories have products *)
Section KleisliProducts.
  Context `(T : @Monad objC C).
  Let KT := KleisliCategory T.

  Local Open Scope morphism_scope.

  Global Instance KleisliCategory_has_is_products
         (X Y : KT)
         `(C_has_product : is_product _ C (T X) (T Y) (T XxY))
         (η := MonadUnit T)
         (µ := MonadMultiplication T)
         (H1 : π₁ = µ X o MorphismOf T (π₁ o η XxY))
         (H2 : π₂ = µ Y o MorphismOf T (π₂ o η XxY))
  : is_product KT X Y XxY.
  refine (@Build_is_product _ KT X Y XxY
                            (π₁ o η XxY)
                            (π₂ o η XxY)
                            (@product_map _ C _ _ _ C_has_product)
                            _
                            _
                            _);
    abstract (
        destruct T;
        clear T;
        (*rename MonadFunctor into T, MonadUnit into η, MonadMultiplication into µ;*)
        repeat match goal with
                 | _ => intro
                 | _ => progress (simpl in *; subst_body)
                 | [ H : _ |- _ ] => (rewrite <- H; rewrite product_fst_commutes; reflexivity)
                 | [ H : _ |- _ ] => (rewrite <- H; rewrite product_snd_commutes; reflexivity)
                 | _ => (apply product_map_unique; simpl)
                 | [ H0 : _, H1 : _ |- _ ] => rewrite <- H0 in H1; exact H1
               end
      ).
  Defined.
