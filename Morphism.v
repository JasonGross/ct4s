Require Import Setoid Utf8 Ensembles ProofIrrelevance Ensembles.
Require Export Notations.

Set Implicit Arguments.

Delimit Scope function_scope with function.

(** * Functions *)
Section morphism.
  Local Open Scope function_scope.

  Definition compose X Y Z (f : Y -> Z) (g : X -> Y) := λ x, f (g x).

  Arguments compose [X Y Z] f g x / .

  Infix "o" := (@compose _ _ _).

  Definition is_injective X Y (f : X -> Y) := forall x y, f x = f y -> x = y.

  Definition sum_function X X' Y Y' (f : X -> Y) (f' : X' -> Y')
  : X ⊔ X' -> Y ⊔ Y'
    := fun x => match x with
                  | inl x => inl (f x)
                  | inr x' => inr (f' x')
                end.

  Section inverse.
    Variables X Y : Type.

    Definition inverse_map (f : X -> Y) : Y -> Ensemble X :=
      fun y => (fun x => y = f x).

    Definition image (f : X -> Y) : Ensemble X -> Ensemble Y :=
      fun Px y => exists x, x ∈ Px /\ f x = y.

    Definition inverse_image (f : X -> Y) : Ensemble Y -> Ensemble X :=
      fun Uy x => (f x) ∈ Uy.

    Definition restrict_domain (f : X -> Y) (P : Ensemble X) : { x | P x } -> Y
      := fun x => f (proj1_sig x).

    Definition restrict (f : X -> Y) (P : Ensemble X) (Q : Ensemble Y) (H : forall x, x ∈ P -> (f x) ∈ Q)
    : { x | P x } -> { y | Q y }
      := fun x => exist _ (f (proj1_sig x)) (H (proj1_sig x) (proj2_sig x)).

    Definition restrict_range (f : X -> Y) (P : Ensemble Y) : sig (inverse_image f P) -> sig P
      := fun x => exist _ (f (proj1_sig x)) (proj2_sig x).
  End inverse.

  Notation "p ⁻¹" := (inverse_image p) (at level 10) : function_scope.

  Section inverse_compose.
    Variables X Y Z : Type.
    Variable f : X -> Y.
    Variable g : Y -> Z.

    Definition inverse_compose_commutes : f ⁻¹ o g ⁻¹ = (g o f) ⁻¹.
      reflexivity.
    Defined.
  End inverse_compose.

  Add Parametric Morphism T : (@Ensembles.In T)
      with signature (@Included T) ==> (@eq T) ==> Basics.impl
        as Included_In_mor.
    repeat intro.
    firstorder.
  Qed.

  Add Parametric Morphism T : (@Ensembles.In T)
      with signature (Basics.flip (@Included T)) ==> (@eq T) ==> (Basics.flip Basics.impl)
        as Included_In_mor'.
    repeat intro.
    firstorder.
  Qed.
End morphism.

