(** * Kleisli category of a monad *)
Section Kleisli.
  (** Given a monad [T := (T, η, µ)], we can form a new category [Kls(T)].

      Definition 5.3.3.1. Let [T = (T, η, µ)] be a monad on
      [Set]. Form a new category, called the [Kleisli category for T],
      denoted [Kls(T)], with sets as objects, [Ob(Kls(T)) := Ob(Set)],
      and with

      [Hom_{Kls}(T((X, Y))) := Hom_{Set}(X, T(Y))]

      for sets [X], [Y]. The identity morphism [id_X : X -> X] in
      [Kls(T)] is given by [η : X -> T(X)] in [Set]. The composition
      of morphisms [f : X -> Y] and [g : Y -> Z] in [Kls(T)] is given
      as follows. Writing them as functions, we have [f : X -> T(Y)]
      and [g : Y -> T(Z)]. The first step is to apply the functor [T]
      to [g], giving [T(g) : T(Y) -> T(T(Z))]. Then compose with [f]
      to get [T(g) o f : X -> T(T(Z))]. Finally, compose with [µ_Z :
      T(T(Z)) -> T(Z)] to get the required function [X -> T(Z)]. The
      associativity of this composition formula follows from the
      associativity law for monads. *)

  Context `(T : @Monad objC C).
  Let η := MonadUnit T.
  Let µ := MonadMultiplication T.

  Local Open Scope morphism_scope.

  Local Ltac simpl_hyp :=
    progress (repeat match goal with
                       | [ H : _ |- _ ] => setoid_rewrite RightIdentity in H
                       | [ H : _ |- _ ] => setoid_rewrite LeftIdentity in H
                       | [ H : _ |- _ ] => setoid_rewrite FIdentityOf in H
                     end).

  Local Ltac safe_t :=
    progress (repeat match goal with
                       | _ => reflexivity
                       | _ => progress autorewrite with morphism
                       | _ => progress simpl_hyp
                       | _ => try_associativity ltac:(rewrite_hyp; autorewrite with morphism; reflexivity)
                       | _ => try_associativity ltac:(rewrite_rev_hyp; autorewrite with morphism; reflexivity)
                       | [ H : _ |- _ ] => simpl in *; try_associativity ltac:(rewrite H; clear H; solve [ safe_t ])
                       | [ H : _ |- _ ] => simpl in *; try_associativity ltac:(rewrite <- H; clear H; solve [ safe_t ])
                     end).

  Local Ltac t :=
    progress (repeat match goal with
                       | _ => progress safe_t
                       | _ => progress repeat rewrite FCompositionOf
                       | _ => progress try_associativity ltac:(apply f_equal2; [ | reflexivity ])
                     end).

  Definition KleisliCategory : @Category objC.
    refine {| Morphism X Y := Morphism C X (T Y);
              Identity X := η X;
              Compose X Y Z g f := (µ Z) o (MorphismOf T g) o f |};
    abstract (
        pose proof (Commutes (MonadUnit T));
        pose proof (Commutes (MonadMultiplication T));
        destruct T; simpl in *; subst_body;
        intros;
        t
      ).
  Defined.
End Kleisli.
