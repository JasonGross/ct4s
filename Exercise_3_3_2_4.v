Section Exercise_3_3_2_4.
  (** ** Problem *)
  (** Let [G] be a graph and consider the set [Path G] of paths in
      [G]. Is there an identity element and a multiplication formula
      for which [Path G] can naturally be given the structure of a
      monoid?  Why or why not? *)
  (** ** Solution *)
  (** Not unless there's only a single vertex in [G], in which case,
      the identity is the empty path, and the formula is path
      composition. Paths can only be composed if the source and target
      line up; in a monoid, anything can be composed with anything
      else. *)

  Definition Exercise_3_3_2_4_SingletonGraphCMonoid
             (G : Graph)
             (v : G)
             (H : forall x : G, x = v)
  : ComputationalMonoid (PathsOf G).
    refine {| id := (existT _ (v, v) NoEdges);
              comp := _ |}.
    intros [[s1 s2] sp] [[d1 d2] dp];
      exists (v, v); simpl in *.
    pose proof (H s1);
    pose proof (H s2);
    pose proof (H d1);
    pose proof (H d2);
    subst; simpl in *.
    exact (concatenate sp dp).
  Defined.

  Instance Exercise_3_3_2_4_SingletonGraphIsMonoid
           (G : Graph)
           (v : G)
           (H : forall x : G, x = v)
  : IsMonoid (@Exercise_3_3_2_4_SingletonGraphCMonoid G v H).
  Proof.
    split;
    repeat (
        let s := fresh in
        let d := fresh in
        intros [[s d] ?];
        pose proof (H s);
        pose proof (H d);
        subst s d;
        simpl in *
      );
    try (apply f_equal || apply f_equal2);
    repeat destruct_eqs;
    auto;
    try rewrite concatenate_associative; reflexivity.
  Qed.

