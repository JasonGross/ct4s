(** * Exercise 5.3.2.5 *)
Module Exercise_5_3_2_5.
  (** ** Problem *)
  (** Let [E] be a set, elements we will refer to as
      $\ensuremath{\text{\emph{exceptions}}}$#<em>exceptions</em>#. We
      imagine that a function [f : X -> Y] either outputs a value or one
      of these exceptions, which might be things like “overflow!” or
      ”division by zero!”, etc. Let [T : Set -> Set] be the functor [X ->
      X ⊔ E]. Follow Example 5.3.2.4 and come up with a unit map [η] and a
      multiplication map [µ] for which [(T, η, µ)] is a monad. *)
  (** ** Solution *)
  (** We have [η] sending [f] to [f ⊔ id], and [µ] collapsing [E ⊔ E]
      to [E] in the obvious way. *)

  (** Everything we need to prove can be proven by repeatedly applying
     [Extensionality_Ensembles : ∀ (U : Type) (A B : Ensemble U),
     Same_set _ A B → A = B], [functional_extensionality_dep : ∀ (A :
     Type) (B : A → Type) (f g : ∀ x : A, B x), (∀ x : A, f x = g x) →
     f = g], unfolding definitions, assuming assumptions, and guessing
     that some inhabitant of a type exists, and leaving it to be
     filled in by a hypothesis later. *)

  Local Ltac t :=
    repeat (apply Extensionality_Ensembles
                  || apply FunctionalExtensionality.functional_extensionality_dep
                  || intro
                  || split
                  || destruct_head_hnf @ex
                  || destruct_head_hnf @and
                  || destruct_head_hnf @sum
                  || (hnf in *; subst)
                  || (repeat esplit; eassumption)).

  Parameter E : Type.

  Definition T : Functor CategoryOfTypes CategoryOfTypes.
    refine (Build_Functor CategoryOfTypes CategoryOfTypes
                          (fun X => (X ⊔ E)%type)
                          (fun _ _ f => f ⊔ id)
                          _
                          _);
    compute;
    abstract t.
  Defined.

  Let η : NaturalTransformation (IdentityFunctor CategoryOfTypes) T.
    refine (Build_NaturalTransformation (IdentityFunctor CategoryOfTypes) T
                                        (fun X => @inl X E)
                                        _).
    compute.
    abstract t.
  Defined.

  Let µ : NaturalTransformation (T o T) T.
    refine (Build_NaturalTransformation (T o T) T
                                        (fun X x => match x with
                                                      | inl (inl x') => inl x'
                                                      | inl (inr x') => inr x'
                                                      | inr x' => inr x'
                                                    end)
                                        _).
    compute;
      abstract t.
  Defined.

  Definition ExceptionMonad : Monad CategoryOfTypes.
    exists T η µ;
    compute; abstract t.
  Defined.
End Exercise_5_3_2_5.
