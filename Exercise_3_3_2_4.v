Ltac destruct_eqs :=
  repeat (unfold eq_sym, eq_rect_r, eq_rect;
          match goal with
            | [ |- context[match ?E with _ => _ end] ] =>
              (let H := fresh in
               set (H := E) in *;
                 clearbody H;
               ((destruct H)
                  || (assert (H = eq_refl) by (apply proof_irrelevance);
                      subst H)))
          end).

