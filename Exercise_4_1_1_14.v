(** * Exercise 4.1.1.14 *)
Section Exercise_4_1_1_14.
  (** ** Problem *)
  (** Suppose that [G = (V, A, src, tgt)] and [G' = (V', A', src',
      tgt')] are graphs and that [f : (f₀, f₁) : G -> G'] is a graph
      homomorphism (as in Definition 3.3.3.1).  If [f₀ : V -> V'] and
      [f₁ : A -> A'] are isomorphisms in [Set], does this imply that
      [f] is an isomorphism in [Grph]? If so, why; and if not, show a
      counterexample (where [f₀] and [f₁] are isomorphisms but [f] is
      not). *)
  (** ** Solution *)
  (** Yes, but this is tricker, because the commutativity squares are non-trivial.  See below. *)

  Let Grph := CategoryOfGraph's.

  (** The proof of commutativity uses the following steps, in some
      order, some number of times:

      - [reflexivity] (that is, [forall x, x = x])
      - unfolding of definitions, in particular of isomorphism
      - using the commutativity diagrams of graph homomorphisms
      - using the fact that, if [f] is invertible, [f ⁻¹ x = f ⁻¹ y]
        implies both [f (f ⁻¹ x) = f (f ⁻¹ y)] and, therefore, [x = y]

      The tactic [t], defined below, expresses this fact. *)
  Local Ltac t :=
    repeat match goal with
               | _ => reflexivity
               | _ => intro
               | [ H := _ |- _ ] => subst H
               | [ H : IsomorphismOf _ _ |- _ ] => destruct H
               | _ => progress (simpl in *; fg_equal)
               | [ H : _ |- _ ] => rewrite H
               | [ H0 : forall x, ?f (?g x) = x,
                     H1 : forall x, ?g (?f x) = x |- ?g ?y = ?z ] =>
                 cut (g (f (g y)) = g (f z));
                   [ let H := fresh in
                     intro H;
                       repeat rewrite H1 in H;
                       exact H
                   | rewrite H0;
                     apply f_equal ]
           end.

  Lemma Exercise_4_1_1_14
        (G G' : Grph)
        (f : Morphism Grph G G')
        (f0 := OnVertices' f)
        (f1 := OnArrows' f)
  : IsomorphismOf CategoryOfTypes f0
    -> IsomorphismOf CategoryOfTypes f1
    -> IsomorphismOf Grph f.
    intros i0 i1.
    eexists {| OnVertices' := (Inverse i0);
               OnArrows' := (Inverse i1) |};
    apply Graph'Homomorphism_Eq; simpl;
    abstract (
        subst f0 f1;
        destruct i0, i1;
        simpl in *;
          fg_equal;
        intuition
      ).
    (** We have left the proofs of commutativity as holes in
        [eexists].  We must now fill them in. *)
    Grab Existential Variables.
    abstract (pose proof (TargetCommutes' f); pose proof (SourceCommutes' f); t).
    abstract (pose proof (TargetCommutes' f); pose proof (SourceCommutes' f); t).
  Defined.
End Exercise_4_1_1_14.

